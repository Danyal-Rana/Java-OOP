public class Program_20 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int c = a + b;
        int d = a - b;
        int e = a * b;
        int f = a / b;
        int g = a % b;
        System.out.println("Sum of a and b is: " + c);
        System.out.println("Difference between a and b is: " + d);
        System.out.println("Product of a and b is: " + e);
        System.out.println("Division of a by b is: " + f);
        System.out.println("Remainder when a is divided by b is: " + g);
    }
}
