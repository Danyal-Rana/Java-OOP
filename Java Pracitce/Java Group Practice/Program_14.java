public class Program_14 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        if (a > b) {
            int c = a * b;
            System.out.println("Product of a and b is: " + c);
        } else {
            int c = a / b;
            System.out.println("Division of a by b is: " + c);
        }
    }
}
