public class Variable_char {
    public static void main (String[] args)
    {
        char x;
        x = 'a';
        System.out.println (x);
        x++;
        System.out.println (x);

        char myVar1 = 67;
        char myVar2 = 68;

        System.out.println (myVar1);
        System.out.println (myVar2);
    }
}